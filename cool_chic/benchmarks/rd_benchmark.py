"""Real RD benchmark: Cool-Chic INR vs libx265 vs libaom-av1.

Encodes the same short clip with each codec at multiple operating points,
and reports a (bpp, PSNR) table plus a CSV/JSON dump.

INR points are generated by sweeping grid capacity (L, T, F). AV1/x265
points come from matching target bitrates so the baselines get to
operate near each INR bpp.
"""
from __future__ import annotations

import argparse
import json
import shutil
import subprocess
import sys
import tempfile
import time
from dataclasses import dataclass, asdict
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[2]))

import torch
import torch.nn.functional as F
from torch.optim import Adam

from cool_chic.codec import VideoINR, VideoConfig
from cool_chic.train_video_qat import (
    overfit_video_qat, VideoQATConfig, reconstruct_quantized_video,
)
from cool_chic.bitstream import encode_codec, KIND_VIDEO
from utils.video_io import read_video


@dataclass
class RDPoint:
    codec: str
    label: str
    bytes: int
    bpp: float
    psnr: float
    seconds: float


def _psnr(x: torch.Tensor, y: torch.Tensor) -> float:
    mse = F.mse_loss(x.clamp(0, 1), y.clamp(0, 1)).clamp_min(1e-12)
    return float(10.0 * torch.log10(1.0 / mse))


def _fp32_overfit(video: torch.Tensor, model: VideoINR, *, steps: int, lr: float,
                   pixels: int, device: str, log_every: int = 400) -> None:
    target_flat = video.to(device).permute(0, 2, 3, 1).reshape(-1, 3)
    N = target_flat.shape[0]
    T, _, H, W = video.shape
    t_d, h_d, w_d = max(T - 1, 1), max(H - 1, 1), max(W - 1, 1)
    opt = Adam(model.parameters(), lr=lr)
    for step in range(steps):
        opt.zero_grad(set_to_none=True)
        idx = torch.randint(0, N, (pixels,), device=device)
        ti = idx // (H * W); rem = idx % (H * W); yi = rem // W; xi = rem % W
        coords = torch.stack([xi.float()/w_d, yi.float()/h_d, ti.float()/t_d], -1)
        pred = model.mlp(model.grid(coords))
        loss = F.mse_loss(pred, target_flat[idx])
        loss.backward(); opt.step()
        if step % log_every == 0 or step == steps - 1:
            ps = -10 * torch.log10(loss.detach().clamp_min(1e-12))
            print(f"    fp32 {step:5d}  loss={float(loss.detach()):.5f}  "
                  f"PSNR~{float(ps):.2f}", flush=True)


def inr_point(video: torch.Tensor, label: str, *,
              L: int, T: int, F_feat: int, N_max: int,
              mlp_hidden: int, mlp_depth: int,
              fp32_steps: int, qat_steps: int, lr: float,
              pixels: int, device: str) -> RDPoint:
    Tf, _, H, W = video.shape
    cfg = VideoConfig(L=L, T=T, F=F_feat, N_min=16, N_max=N_max,
                       mlp_hidden=mlp_hidden, mlp_depth=mlp_depth)
    print(f"\n[INR {label}] cfg L={L} T={T} F={F_feat} N_max={N_max} "
          f"mlp={mlp_hidden}x{mlp_depth}")
    model = VideoINR(T_frames=Tf, H=H, W=W, cfg=cfg).to(device)
    print(f"  params: {model.total_params}  ({model.total_params * 4 / 1024:.1f} KB fp32)")

    t0 = time.time()
    _fp32_overfit(video, model, steps=fp32_steps, lr=lr, pixels=pixels,
                   device=device, log_every=max(fp32_steps // 5, 1))

    quants, _ = overfit_video_qat(video, model,
                                    qcfg=VideoQATConfig(steps=qat_steps,
                                                        lr=lr * 0.4,
                                                        scale_lr=5e-3,
                                                        lambda_rate=0.0,
                                                        pixels_per_step=pixels,
                                                        log_every=max(qat_steps // 5, 1),
                                                        init_scale=5e-3),
                                    device=device, verbose=True)

    recon = reconstruct_quantized_video(model, quants).clamp(0, 1).cpu()
    ps = _psnr(recon, video.cpu())
    blob = encode_codec(quants, kind=KIND_VIDEO, H=H, W=W, T_frames=Tf, cfg=cfg)
    bytes_ = len(blob)
    bpp = bytes_ * 8 / (Tf * H * W)
    elapsed = time.time() - t0
    print(f"  -> {bytes_} B  bpp={bpp:.4f}  PSNR={ps:.2f} dB  "
          f"({elapsed:.1f}s)")
    return RDPoint("inr", label, bytes_, bpp, ps, elapsed)


def _encode_ffmpeg(codec: str, src: str, kbps: int, out_dir: Path,
                    preset: str = "medium", passes: int = 1) -> tuple[Path, float]:
    """Returns (output mp4 path, wall seconds)."""
    t0 = time.time()
    out = out_dir / f"{codec}_{kbps}.mp4"
    common = ["ffmpeg", "-hide_banner", "-loglevel", "error", "-y", "-i", src,
              "-pix_fmt", "yuv420p", "-an"]
    if codec == "x265":
        cmd = common + ["-c:v", "libx265", "-preset", preset,
                          "-b:v", f"{kbps}k", str(out)]
    elif codec == "av1":
        cmd = common + ["-c:v", "libaom-av1", "-cpu-used", "6",
                          "-b:v", f"{kbps}k", str(out)]
    else:
        raise ValueError(codec)
    subprocess.run(cmd, check=True)
    return out, time.time() - t0


def ffmpeg_point(codec: str, src: str, kbps: int, video: torch.Tensor,
                  out_dir: Path) -> RDPoint:
    print(f"\n[{codec} @ {kbps}kbps]")
    mp4, wall = _encode_ffmpeg(codec, src, kbps, out_dir)
    bytes_ = mp4.stat().st_size
    Tf, _, H, W = video.shape
    bpp = bytes_ * 8 / (Tf * H * W)

    recon = read_video(str(mp4), max_frames=Tf)
    n = min(Tf, recon.shape[0])
    ps = _psnr(recon[:n].cpu(), video[:n].cpu())
    print(f"  -> {bytes_} B  bpp={bpp:.4f}  PSNR={ps:.2f} dB  ({wall:.1f}s)")
    return RDPoint(codec, f"{kbps}k", bytes_, bpp, ps, wall)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("source", type=str)
    ap.add_argument("--frames", type=int, default=16)
    ap.add_argument("--device", type=str,
                    default="cuda" if torch.cuda.is_available() else "cpu")
    ap.add_argument("--out", type=str, default="cool_chic/benchmarks/out")
    ap.add_argument("--skip-inr", action="store_true")
    ap.add_argument("--skip-ffmpeg", action="store_true")
    ap.add_argument("--fp32-steps", type=int, default=2000)
    ap.add_argument("--qat-steps", type=int, default=1500)
    ap.add_argument("--lr", type=float, default=5e-3)
    ap.add_argument("--pixels", type=int, default=1 << 13)
    args = ap.parse_args()

    out_dir = Path(args.out); out_dir.mkdir(parents=True, exist_ok=True)

    video = read_video(args.source, max_frames=args.frames)
    Tf, _, H, W = video.shape
    total_px = Tf * H * W
    print(f"source: {args.source}  {Tf}x{W}x{H}  ({total_px} pixels)")

    points: list[RDPoint] = []

    if not args.skip_inr:
        # Small / medium / large INR configurations to sweep the RD curve.
        inr_configs = [
            dict(label="tiny",   L=4, T=1<<11, F_feat=2, N_max=128,
                 mlp_hidden=32, mlp_depth=3),
            dict(label="small",  L=5, T=1<<12, F_feat=2, N_max=256,
                 mlp_hidden=48, mlp_depth=3),
            dict(label="medium", L=6, T=1<<13, F_feat=2, N_max=256,
                 mlp_hidden=64, mlp_depth=4),
        ]
        for c in inr_configs:
            p = inr_point(video, **c,
                            fp32_steps=args.fp32_steps, qat_steps=args.qat_steps,
                            lr=args.lr, pixels=args.pixels, device=args.device)
            points.append(p)

    if not args.skip_ffmpeg:
        # Clip the source to the exact frame count used by the INR encoder,
        # so bpp is computed over the same pixel budget.
        clipped = out_dir / "source_clipped.mp4"
        subprocess.run(["ffmpeg", "-hide_banner", "-loglevel", "error", "-y",
                        "-i", args.source, "-frames:v", str(Tf), "-an",
                        "-c:v", "libx264", "-crf", "6", "-preset", "veryslow",
                        str(clipped)], check=True)

        for kbps in [100, 250, 500, 1000, 2000]:
            points.append(ffmpeg_point("x265", str(clipped), kbps, video, out_dir))
        for kbps in [100, 250, 500, 1000, 2000]:
            points.append(ffmpeg_point("av1",  str(clipped), kbps, video, out_dir))

    print("\n=== RD TABLE ===")
    print(f"{'codec':6s} {'label':10s} {'bytes':>8s} {'bpp':>8s} {'PSNR':>6s} {'sec':>6s}")
    for p in sorted(points, key=lambda p: (p.codec, p.bpp)):
        print(f"{p.codec:6s} {p.label:10s} {p.bytes:>8d} {p.bpp:>8.4f} "
              f"{p.psnr:>6.2f} {p.seconds:>6.1f}")

    # Merge with existing rd_points.json if present (so separate INR-only /
    # ffmpeg-only invocations accumulate instead of clobbering each other).
    json_path = out_dir / "rd_points.json"
    existing = []
    if json_path.exists():
        try:
            existing = json.loads(json_path.read_text())
        except Exception:
            existing = []
    new_keys = {(p.codec, p.label) for p in points}
    merged = [e for e in existing if (e.get("codec"), e.get("label")) not in new_keys]
    merged.extend(asdict(p) for p in points)
    json_path.write_text(json.dumps(merged, indent=2))
    print(f"\nwrote {json_path} ({len(merged)} points)")


if __name__ == "__main__":
    main()
